#Create function to return sum of first n natural numbers
def sumN(n):
	sum = 0
	for i in (range(n)):
		sum = sum + sum[i]
	return(sum)


def sumNcubes(n):
	sum = 0
	for i in (range(n)):
		sum = sum + (sum[i])**3
	return(sum)


